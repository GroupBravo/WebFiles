$(document).ready(function(){
  $("#StartPoint").keyup(function(){
    $("#clearstart").toggle(Boolean($(this).val()));
  });
  $("#clearstart").toggle(Boolean($("#StartPoint").val()));
  $("#clearstart").click(function(){
    $("#StartPoint").val('').focus();
    $(this).hide();
  });
});
